        private void crearEmpleado() {
                EmpleadoDAO empleadoDAO = new EmpleadoDAO();
                NominaDAO nominaDAO = new NominaDAO();

                try {
                        // Obtener datos desde los campos del formulario
                        int idEmpleado = Integer.parseInt(ObtenerIU.obtenerTextoCampo(tfIdentificacion));
                        String nombre = ObtenerIU.obtenerTextoCampo(tfNombre);
                        String apellido = ObtenerIU.obtenerTextoCampo(tfApellido);
                        double salario = Double.parseDouble(ObtenerIU.obtenerTextoCampo(tfSalario));
                        String fechaContratacion = ObtenerIU.obtenerTextoCampo(tfFechaContrato);
                        String cargo = ObtenerIU.obtenerSeleccionCombo(cbCargo);
                        String telefono = ObtenerIU.obtenerTextoCampo(tfTelefono);
                        String email = ObtenerIU.obtenerTextoCampo(tfCorreo);

                        // Crear objeto Empleado con los datos actualizados
                        Empleado empleado = new Empleado();
                        empleado.setIdEmpleado(idEmpleado);
                        empleado.setNombre(nombre);
                        empleado.setApellido(apellido);
                        empleado.setSalario(salario);
                        empleado.setFechaContratacion(fechaContratacion);
                        empleado.setCargo(cargo);
                        empleado.setTelefono(telefono);
                        empleado.setEmail(email);

                        // Insertar el empleado en la base de datos
                        boolean resultadoEmpleado = empleadoDAO.insertar(empleado);

                        if (resultadoEmpleado) {
                                // Crear un registro inicial en la tabla Nomina
                                Nomina nomina = new Nomina();
                                                                          // Colombia - ajustar seg√∫n sea necesario

                                // Configurar la n√≥mina con valores predeterminados
                                nomina.setFecha(fechaContratacion); // Usar la misma fecha de contrataci√≥n
                                nomina.setSalarioBase(salario);
                                nomina.setDeducciones(0.0);
                                nomina.setBonificaciones(0.0);
                                nomina.setIdEmpleado(idEmpleado);

                                // Insertar la n√≥mina en la base de datos
                                boolean resultadoNomina = nominaDAO.insertar(nomina);

                                if (resultadoNomina) {
                                        JOptionPane.showMessageDialog(this,
                                                        "El empleado se registr√≥ correctamente y se cre√≥ su registro de n√≥mina inicial.",
                                                        "Registro exitoso", JOptionPane.INFORMATION_MESSAGE);
                                } else {
                                        JOptionPane.showMessageDialog(this,
                                                        "El empleado se registr√≥, pero no se pudo crear su registro de n√≥mina inicial.",
                                                        "Advertencia", JOptionPane.WARNING_MESSAGE);
                                }

                                // Redirigir o refrescar vista
                                PayrollManagementScreen mu = new PayrollManagementScreen();
                                mu.setVisible(true);
                                this.setVisible(false);
                        } else {
                                JOptionPane.showMessageDialog(this, "No se pudo registrar el empleado.", "Error",
                                                JOptionPane.ERROR_MESSAGE);
                        }

                } catch (Exception e) {
                        JOptionPane.showMessageDialog(this,
                                        "Ocurri√≥ un error al registrar el empleado: " + e.getMessage(), "Error",
                                        JOptionPane.ERROR_MESSAGE);
                }
        }

        private boolean datosValidos() {
                return Verificar.validarNombre(ObtenerIU.obtenerTextoCampo(tfNombre))
                                && Verificar.validarNombre(ObtenerIU.obtenerTextoCampo(tfApellido))
                                && Verificar.validarYVerificarCedulaEmpleado(
                                                ObtenerIU.obtenerTextoCampo(tfIdentificacion))

                                && Verificar.validarCorreo(ObtenerIU.obtenerTextoCampo(tfCorreo))
                                && Verificar.validarComboBox((ObtenerIU.obtenerSeleccionCombo(cbCargo)))
                                && Verificar.validarSalario(ObtenerIU.obtenerTextoCampo(tfSalario))
                                && Verificar.validarFechaMenorIgualHoy(ObtenerIU.obtenerTextoCampo(tfFechaContrato))
                                && Verificar.validarTelefono((ObtenerIU.obtenerTextoCampo(tfTelefono)));

        }

