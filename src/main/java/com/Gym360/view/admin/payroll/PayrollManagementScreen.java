        private void filtrarTablaEmpleados() {
                String filtro = tfBuscar.getText().trim();
                cargarTablaEmpleados(filtro);
        }

        public void cargarTablaEmpleados(String filtro) {
                EmpleadoDAO empleadoDAO = new EmpleadoDAO();
                List<Empleado> empleados;

                if (filtro == null || filtro.trim().isEmpty()) {
                        empleados = empleadoDAO.obtenerTodos();
                } else {
                        empleados = empleadoDAO.obtenerPorIdFiltro(filtro); // Método adaptado al campo "nombre"
                }

                // Columnas basadas en la nueva estructura de la tabla Empleado
                String[] columnas = { "ID Empleado", "Nombre", "Apellido", "Cargo", "Teléfono", "Email", "Salario",
                                "Fecha de Contratación" };
                DefaultTableModel model = new DefaultTableModel(columnas, 0);

                for (Empleado emp : empleados) {
                        Object[] fila = { emp.getIdEmpleado(), emp.getNombre(), emp.getApellido(), emp.getCargo(),
                                        emp.getTelefono(), emp.getEmail(), emp.getSalario(),
                                        emp.getFechaContratacion() };
                        model.addRow(fila);
                }

                tTablaEmpleadosNomina.setModel(model); // Asegúrate de que esta tabla esté bien inicializada
        }

